"""每日盤前金句(對句梗)——改編自投資名人的名言,做成有趣的兩句對仗,放片頭/片尾。

正式日更由雲端 routine 每天現生一句(prompt 有指示);此處是確定性後備,依日期輪替,
確保 pipeline 永遠有一句。出處都是真實投資名人,只是把原意改寫成順口的梗。
"""

from __future__ import annotations

import datetime as dt

# (上句, 下句, 出處名人)——下句梗自該名人的著名觀念
_PUNCHLINES: list[tuple[str, str, str]] = [
    ("別人貪婪我恐懼", "別人恐懼我撿屍", "巴菲特"),
    ("退潮的時候", "才知道誰沒穿褲子", "巴菲特"),
    ("凡事反過來想", "先求別賠光,再談發大財", "蒙格"),
    ("不懂的別亂買", "買了也別嘴硬硬凹", "彼得·林區"),
    ("短線是投票機", "長線是體重計", "葛拉漢"),
    ("市場先生愛起乩", "別被他的情緒勒索", "葛拉漢"),
    ("別大海撈那根針", "整捆乾草抱回家", "約翰·柏格"),
    ("會買的是徒弟", "會賣的才是師傅", "傑西·李佛摩"),
]


def punchline_for(date: dt.date) -> tuple[str, str, str]:
    """依日期確定性挑一句(每天輪替,不重複到天荒地老)。"""
    return _PUNCHLINES[date.toordinal() % len(_PUNCHLINES)]
